// Khai báo 3 biến với kiểu dữ liệu number và gán giá trị cho hai biến đầu tiên
let num1: number = 10;
let num2: number = 5;
let result: number;

// Thực hiện các phép tính toán cơ bản và gán kết quả cho biến thứ 3
result = num1 + num2;
console.log("Tổng của hai số là:", result);

result = num1 - num2;
console.log("Hiệu của hai số là:", result);

result = num1 * num2;
console.log("Tích của hai số là:", result);

result = num1 / num2;
console.log("Thương của hai số là:", result);

result = num1 % num2;
console.log("Phần dư của hai số là:", result);
